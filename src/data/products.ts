const img = (seed: string, alt: string, categorySlug?: string) => ({
  id: seed,
  url: getProductImageUrl(seed, categorySlug),
  alt,
});

function makeProduct(
  partial: Omit<Product, "images" | "specs" | "tags" | "createdAt"> & {
    imageSeed: string;
    imageUrl?: string;
    specs?: Product["specs"];
    tags?: string[];
  },
): Product {
  const primary = partial.imageUrl
    ? { id: partial.imageSeed, url: partial.imageUrl, alt: partial.name }
    : img(partial.imageSeed, partial.name, partial.categorySlug);
  return {
    ...partial,
    images: partial.imageUrl
      ? [
          primary,
          {
            id: `${partial.imageSeed}-2`,
            url: partial.imageUrl,
            alt: `${partial.name} side`,
          },
        ]
      : [
          primary,
          img(
            `${partial.imageSeed}-2`,
            `${partial.name} side`,
            partial.categorySlug,
          ),
        ],
    specs: partial.specs ?? [
      { label: "Warranty", value: "2 Years" },
      { label: "Condition", value: "Brand New" },
    ],
    tags: partial.tags ?? [],
    createdAt: "2026-01-15",
  };
}

